public class Pessoa {
    private String nome;
    private int idade;

    public void setNome(String Nome){
        this.nome = Nome;
    }

    public void setIdade(int Idade){
        this.idade = Idade;
    }

    public String getNome(){
        return this.nome;
    }

    public int getIdade(){
        return this.idade;
    }
}
